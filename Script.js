function showmn() {
  const tabbar = document.querySelector(".menu-bar").style;
  tabbar.left=0;
  tabbar.transition="left 0.3s ease-in-out"
}

function closemn() {
  const tabbar = document.querySelector(".menu-bar").style;
  tabbar.left="-160px";
  tabbar.transition="left 0.3s ease-in-out"
}

